function theBeatlesPlay(musicians,instruments) {
  var arrayBeatles = []
  for (let i=0; i < musicians.length; i++) {
    var x = musicians[i]
    for (let j=0; j < instruments.length; j++) {
      var y = instruments[j]
    }
  var stringBeatles = x + 'plays' + y
  arrayBeatles.push(stringBeatles)
  }
  return arrayBeatles
}
